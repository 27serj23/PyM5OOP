import random


class Warrior:
    def __init__(self, name):
        self.name = name
        self.health = 100
        self.endurance = 100
        self.armor = 100

    def take_damage(self, amount):
        if self.armor > 0:
            armor_damage = min(amount // 2, self.armor)
            health_damage = max(0, amount - armor_damage)
            self.armor -= armor_damage
            self.health -= health_damage
        else:
            self.health -= amount

    def attack(self, opponent):
        damage = random.randint(10, 30)
        opponent.take_damage(damage)
        self.endurance -= 10
        print(f"‚öî {self.name} –∞—Ç–∞–∫–æ–≤–∞–ª {opponent.name} | –£—Ä–æ–Ω: {damage}")
        print(f"   ‚ù§ {opponent.name}: –∑–¥–æ—Ä–æ–≤—å–µ {opponent.health}, –±—Ä–æ–Ω—è {opponent.armor}")

    def defend(self, opponent):
        incoming_damage = random.randint(0, 20)
        armor_damage = random.randint(0, 10)
        self.take_damage(incoming_damage)
        self.armor -= armor_damage
        print(f"üõ° {self.name} –∑–∞—â–∏—Ç–∏–ª—Å—è –æ—Ç {opponent.name}")
        print(f"   –ü–æ—Ç–µ—Ä—è–Ω–æ: –∑–¥–æ—Ä–æ–≤—å–µ {incoming_damage}, –±—Ä–æ–Ω—è {armor_damage}")

    def act(self, opponent):
        action = random.choice(['attack', 'defend'])
        if action == 'attack':
            self.attack(opponent)
        elif action == 'defend':
            self.defend(opponent)


